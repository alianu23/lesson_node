const plus = (a) => {
  return a + 10;
};
const minus = (a) => {
  return a - 11;
};
const multiple = (a) => {
  return a * 11;
};
const divide = (a) => {
  return a / 10;
};
const power = (a) => {
  return a ** 3;
};
const squareRoot = (a) => {
  return Math.sqrt(a);
};

module.exports = { plus, minus, multiple, divide, power, squareRoot };
